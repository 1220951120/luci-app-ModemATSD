#!/bin/sh

MODEM_LED_STATE_FILE="${MODEM_LED_STATE_FILE:-/tmp/modconf-AK68.conf}"
MODEM_NU313_STATE_FILE="${MODEM_NU313_STATE_FILE:-/tmp/modconf-NU313.conf}"
MODEM_LED_SYSFS_ROOT="${MODEM_LED_SYSFS_ROOT:-/sys/class/leds}"

# The physical LEDs are shared by the external ATSD modem and the onboard
# NU313.  Resolve ownership before every write so manual actions, schedules
# and background status loops all follow the same rule.
modem_led_owner_from_flags() {
	local atsd_detected="$1"
	local nu313_detected="$2"

	if [ "$atsd_detected" = 1 ]; then
		echo atsd
	elif [ "$nu313_detected" = 1 ]; then
		echo nu313
	else
		echo atsd
	fi
}

modem_led_atsd_detected() {
	local state
	state="$(cat "$MODEM_LED_STATE_FILE" 2>/dev/null)"
	case "$state" in
		*RM520N*|*MT5700*|*NU313*) return 0 ;;
		*) return 1 ;;
	esac
}

modem_led_nu313_detected() {
	local state
	state="$(sed -n '1p' "$MODEM_NU313_STATE_FILE" 2>/dev/null)"
	case "$state" in
		*NU313*) return 0 ;;
		*) return 1 ;;
	esac
}

modem_led_owner() {
	local atsd_detected=0
	local nu313_detected=0
	local now
	now="$(date +%s)"
	case "${MODEM_LED_OWNER_CACHE_TIME:-}:$now" in
		*[!0-9:]*|:*) ;;
		*)
			if [ $((now - MODEM_LED_OWNER_CACHE_TIME)) -le 2 ] 2>/dev/null; then
				echo "$MODEM_LED_OWNER_CACHE_VALUE"
				return
			fi
			;;
	esac
	modem_led_atsd_detected && atsd_detected=1
	modem_led_nu313_detected && nu313_detected=1
	MODEM_LED_OWNER_CACHE_VALUE="$(modem_led_owner_from_flags "$atsd_detected" "$nu313_detected")"
	MODEM_LED_OWNER_CACHE_TIME="$now"
	echo "$MODEM_LED_OWNER_CACHE_VALUE"
}

modem_led_has_control() {
	modem_led_owner >/dev/null
	[ "$MODEM_LED_OWNER_CACHE_VALUE" = atsd ]
}

# Keep ModemATSD compatible with both the legacy vendor LED labels and the
# color/function names generated by current device-tree bindings.
modem_led_names() {
	case "$1" in
		cmode4) echo "blue:cmode4 hc:blue:cmode4" ;;
		cmode5) echo "blue:cmode5 hc:blue:cmode5" ;;
		wifi)   echo "blue:wlan hc:blue:wifi" ;;
		status) echo "blue:status hc:blue:status" ;;
		sig1)   echo "blue:sig1 hc:blue:sig1" ;;
		sig2)   echo "blue:sig2 hc:blue:sig2" ;;
		sig3)   echo "blue:sig3 hc:blue:sig3" ;;
		int)    echo "blue:indicator hc:blue:int" ;;
		*) return 1 ;;
	esac
}

modem_led_set() {
	local value="$1"
	local logical sysfs brightness

	modem_led_has_control || return 3

	case "$value" in
		0|1) ;;
		*) return 2 ;;
	esac
	shift

	for logical in "$@"; do
		for sysfs in $(modem_led_names "$logical"); do
			brightness="$MODEM_LED_SYSFS_ROOT/$sysfs/brightness"
			[ -w "$brightness" ] && echo "$value" > "$brightness"
		done
	done
	return 0
}

if [ "${0##*/}" = "modem-led-control-AK68.sh" ]; then
	case "${1:-}" in
		owner)
			[ "$#" -eq 1 ] || exit 2
			modem_led_owner
			;;
		0|1)
			[ "$#" -ge 2 ] || exit 2
			modem_led_set "$@" || {
				echo "当前 LED 控制权在 NU313，ATSD 未执行灯光操作。" >&2
				exit 3
			}
			;;
		*)
			echo "Usage: $0 owner | {0|1} LED..." >&2
			exit 2
			;;
	esac
fi
